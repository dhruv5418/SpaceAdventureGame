package game;

/**
 * Interface which contains all the method for attack.
 */
public interface Attackable {
    /**
     * public method of interface for attack
     */
    public void attack();
    /**
     * public method of interface for choosing space ship
     */
    public void chooseSpaceShip();
    /**
     * public method of interface for confirmation of attack.
     */
    public void confirmAttack();
}
